function Accomodation() {
  var accList = JSON.parse(Cookies.get("hotel"));

  var addcontrols = "<tr>";
  addcontrols += "<td class = 'tblRow'>" + accList["nome"] + "</td>";
  addcontrols += "<td class = 'tblRow'>" + accList["indirizzo"] + "</td>";
  if (accList["stelle"] == 0) {
    addcontrols += "<td class = 'tblRow'>N.D.</td>";
  } else {
    addcontrols += "<td class = 'tblRow'>" + accList["stelle"] + "</td>";
  }
  if (accList.hasOwnProperty("sito")) {
    addcontrols += "<td class = 'tblRow'>" + accList["sito"] + "</td>";
  } else {
    addcontrols += "<td class = 'tblRow'>N.D.</td>";
  }
  if (accList.hasOwnProperty("telefono")) {
    addcontrols += "<td class = 'tblRow'>" + accList["telefono"] + "</td>";
  } else {
    addcontrols += "<td class = 'tblRow'>N.D.</td>";
  }
  if (accList.hasOwnProperty("email")) {
    addcontrols += "<td class = 'tblRow'>" + accList["email"] + "</td>";
  } else {
    addcontrols += "<td class = 'tblRow'>N.D.</td>";
  }
  addcontrols += "</tr>";
  $("#tbl_accom tbody").append(addcontrols);
}

function Activity() {
  var actvList = JSON.parse(Cookies.get("selected"));
  for (var i = 0; i < actvList.length; i++) {
    var addcontrols = "<tr>";
    addcontrols += "<td class = 'tblRow'>" + actvList[i]["name"] + "</td>";
    addcontrols += "<td class = 'tblRow'>" + actvList[i]["address"] + "</td>";
    if (actvList[i]["opening_hours"] == "") {
      addcontrols += "<td class = 'tblRow'>N.D.</td>";
    } else {
      addcontrols +=
        "<td class = 'tblRow'>" + actvList[i]["opening_hours"] + "</td>";
    }
    if (actvList[i]["phone"] != "") {
      addcontrols += "<td class = 'tblRow'>" + actvList[i]["phone"] + "</td>";
    } else {
      addcontrols += "<td class = 'tblRow'>N.D.</td>";
    }
    if (actvList[i]["cost"] != "") {
      addcontrols += "<td class = 'tblRow'>" + actvList[i]["cost"] + "</td>";
    } else {
      addcontrols += "<td class = 'tblRow'>N.D.</td>";
    }
    if (actvList[i]["website"] != "") {
      addcontrols += "<td class = 'tblRow'>" + actvList[i]["website"] + "</td>";
    } else {
      addcontrols += "<td class = 'tblRow'>N.D.</td>";
    }
    addcontrols += "</tr>";
    $("#tbl_activity tbody").append(addcontrols);
  }
}

function loadMap() {
  map = new L.Map("myMap");
  L.tileLayer("http://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
    maxZoom: 18,
  }).addTo(map);
  map.attributionControl.addAttribution('<a href="http://www.openstreetmap.org/copyright">&copy; OpenStreetMap</a>');
  var city = new L.LatLng(45.5582, 11.5419);
  map.setView(city, 12);
}

